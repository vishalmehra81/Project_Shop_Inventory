class Product:
    def __init__(self, name, brand, stock, category, size, cost_price, selling_price, id=None):

        self.name = name
        self.brand = brand
        self.stock = stock
        self.category = category
        self.size = size
        self.cost_price = cost_price
        self.selling_price = selling_price
        self.id = id 
